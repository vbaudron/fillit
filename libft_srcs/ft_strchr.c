#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*res;

	i = 0;
	while (s[i] && s[i] != c)
		i++;
	if (s[i] == c)
	{
		res = (char*)&s[i];
		return (res);
	}
	else
		return (NULL);
}
