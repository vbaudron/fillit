#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*res;

	i = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	res = (char*)malloc(sizeof(*res) * ft_strlen(s) + 1);
	if (res == NULL)
		return (NULL);
	while (s[i])
	{
		res[i] = f(s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
