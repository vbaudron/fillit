#include "libft.h"
#include "fillit.h"

size_t		ft_nb_tetri(char *map)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (map[i + j])
	{
		while (map[i + j] != '\n')
			i++;
		while (map[i + j] == '\n')
			j++;
	}
	return (i / 16);
}
